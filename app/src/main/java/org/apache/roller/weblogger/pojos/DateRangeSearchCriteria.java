package org.apache.roller.weblogger.pojos;

import java.util.Date;

/**
 * Base class for search criteria that include a date range and a specific
 * weblog.
 */
public class DateRangeSearchCriteria extends BaseSearchCriteria {

    // Weblog or null to get generic results
    protected Weblog weblog;

    // Start date or null for no restriction
    protected Date startDate;

    // End date or null for no restriction
    protected Date endDate;

    public Weblog getWeblog() {
        return weblog;
    }

    public void setWeblog(Weblog weblog) {
        this.weblog = weblog;
    }

    public Date getStartDate() {
        return startDate;
    }

    public void setStartDate(Date startDate) {
        this.startDate = startDate;
    }

    public Date getEndDate() {
        return endDate;
    }

    public void setEndDate(Date endDate) {
        this.endDate = endDate;
    }
}
